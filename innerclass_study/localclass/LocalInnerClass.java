package localclass;
//如果需要用局部内部类定义变量，创建实例或者派生子类，都只能在局部内部类所在的方法内进行。
public class LocalInnerClass {
	public static void main(String[] args) {
		//定义局部内部类
		class InnerBase{
			int a;
		}
		//定义局部内部类的子类
		class InnerSub extends InnerBase{
			int b;
		}
		//创建局部内部类的对象
		InnerSub is = new InnerSub();
		is.a = 5;
		is.b = 8;
		System.out.println("a:"+is.a +"\t"+"b:"+is.b);
	}

}
